package com.learnjava;

public class Conditions {

	public static void main(String[] args) {
		int a = 50;
		int b = 40;
		
		int c = a+b;
		
		if(c>100) {
			System.out.println("Best quality");
		}
		else if(c>60) {
			System.out.println("Satisfactory");
		}
		else {
			System.out.println("Unknown");
		}
	}
	

}
